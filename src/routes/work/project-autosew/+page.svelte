        </div>
        <p class="mb-12 mt-6 text-lg">
          The third step was placement, users were provided with a comprehensive
          set of tools, including a ruler and marking tool. I incorporated
          numerous images to enhance clarity regarding the functionality of each
          tool. This phase was the final step before letting the machine run on
          it own. The steps were kept simple enough for users to follow
          intuitively and we found that user just press “Start Quilting” when
          they follow the steps on there own.
