          <h3 class="text-3xl font-semibold mt-5">1. Set Safe Zone</h3>
        </div>
        <p class="mb-12 mt-6 text-lg">
          The safe zone was the first step; it was the crucial prerequisite
          before anything could happen with the software, as the machine needs
          to know where to go. Of course, there were some questions about the
          naming, such as whether 'quilt area' was more viable because it was
          more literal. However, we kept 'safe zone' due to its connection with
          previous users and their association with the product.
        </p>
      </span>
    </span>
  </div>
</div>

<div class="2xl:mx-40 xl:mx-20 lg:mx-10 mx-5 my-20">
