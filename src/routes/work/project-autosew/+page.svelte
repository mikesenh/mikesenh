          <h3 class="text-3xl font-semibold">2. Pick a Pattern</h3>
        </div>
        <p class="mb-6 mt-6 text-lg">
          In the original software, all features were accessible from the home
          screen, and interestingly, this remains true. However, through UX
          research, we discovered that placing the pattern picker as the second
          step was more practical. It turned out to be the initial action users
          took when getting on the machine. Of course, the safe zone came first,
          ensuring the machine knows where to commence quilting or sewing.
        </p>
