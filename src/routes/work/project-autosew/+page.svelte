<div class="2xl:mx-40 xl:mx-20 lg:mx-10 mx-5 my-20">
  <div class="border-b-4 border-myblue pb-3">
    <h3 class="text-3xl font-semibold">The Simple & Easy - 3 Step System!</h3>
  </div>
  <p class="mb-12 mt-6 text-lg">
    Originally, there was going to be a quilting wizard that spoke to the user
    on the left side of the screen, guiding everyone through the user-friendly
    software. After extensive consideration and UX research, we discovered that
    the best option was to isolate each feature into steps. Once the user has
    chosen a step, they would proceed to the next one. This approach was quite
    ingenious because it allowed us to get someone started in three easy steps
    without the need for extensive teaching.
  </p>
</div>

