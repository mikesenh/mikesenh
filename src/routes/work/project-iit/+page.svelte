    The goal of the website was not just to present the toys as mere playthings, but to demonstrate that they were meant to be anything the user desired. By highlighting the versatility and potential for creative expression, I sought to convey the message that being creative is about seeing the possibilities and feeling inspired.  I wanted the visuals of the toys to serve as a practical guide for instructions, enabling users to easily understand the various ways in which the toys could be connected. By showcasing the toys in diverse shapes and configurations, I aimed to spark the imagination of visitors and encourage them to explore the endless possibilities.


