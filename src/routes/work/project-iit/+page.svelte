        When designing the toy website, my main focus was to create a visual representation that would resonate with both parents and children. To achieve this, I incorporated playful curves as the very first concept. I believed that these wavy lines would evoke a smooth and innocent feel, inviting visitors to explore the website. One of my goals was to establish a repeating theme throughout the website, making it iconic and memorable. While this concept is yet to be tested, the initial rendering of the design felt good and aligned with my primary objective. I aimed to create a system where each page would have its own unique touch while still being part of the same cohesive family.
