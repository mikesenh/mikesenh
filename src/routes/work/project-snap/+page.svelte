    </div>
    <p class="mb-6 mt-6 text-lg">
      For this brochure, consistency was key. The sides, top, and bottom margins were carefully designed to create a balanced and cohesive layout. Specifically, I used 24px margins at the top and bottom to frame the content and give it a clean, professional feel. To ensure clarity and readability, I implemented a systematic approach to spacing:
<br><br>
7px spacing was applied to group related elements, maintaining a visual connection between them. <br>
16px spacing was used to separate distinct sections, creating clear boundaries and making the information easier to navigate. <br>
<br>
These deliberate choices are evident in the detailed spacing rulers and grids I incorporated into the design. This level of precision not only enhances the brochure’s professionalism but also ensures that every element feels intentional and well-organized. It’s a testament to how thoughtful spacing can elevate the overall user experience.

