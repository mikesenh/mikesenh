      </h3>
    </div>
    <p class="mb-6 mt-6 text-lg">
      My goal was to present the information in a simple, digestible way. I broke down the content into clear sections, using bold, colorful titles to highlight key topics and placing the corresponding details below. All the information was sourced directly from official SNAP documents, but I reorganized and condensed it into one cohesive brochure that covers the essentials. <br>
  I paid close attention to layout details, ensuring the margins and spacing created a logical hierarchy of information. This makes it easy for the eye to follow and helps users quickly find what they need. By grouping related content into visually distinct blocks, I maintained clarity without sacrificing the design’s appeal. The result is a brochure that’s not only functional but also visually engaging—keeping the information accessible and interesting.
