      Sustainable Design: Crafting a Menu with Purpose
    </h3>
  </div>
  <p class="mb-12 mt-6 text-lg">
    I decided to explore the idea of printing the menu on cardboard—a choice that not only saves on materials but also adds a unique, sturdy feel to the design. This approach aligns with eco-friendly principles, emphasizing the use of natural, sustainable materials. The texture and durability of cardboard give the menu a tactile quality that enhances the user experience, while also making a subtle statement about conscious design.
This decision reflects my belief that good design should be both functional and thoughtful. By choosing materials that are environmentally friendly and visually appealing, the menu speaks for itself, showcasing how small choices can have a big impact. It’s a testament to how design can be innovative, practical, and sustainable all at once.
